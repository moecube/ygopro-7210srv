function combo_pack.packcheck()
	for _,pack in ipairs(combo_pack.pack) do
		local g=Group.CreateGroup()
		for _,code in ipairs(pack) do
			local card=Duel.CreateToken(0,code)
			g:AddCard(card)
		end
		Duel.SendtoDeck(g,nil,0,REASON_RULE)
		Duel.ConfirmCards(0,g)
		Duel.Exile(g,REASON_RULE)
	end
end

